class Solution {
  public:
    // Function to rotate a linked list.
    Node* rotate(Node* head, int k) {
        // Your code here
        Node* cur=head;
        int total=0;
        while(cur!=nullptr){
            if(cur->next==nullptr){
                cur->next=head;
                total++;
                break;
            }
            cur=cur->next;
            total++;
        }
        
        k%=total;
        cur=head;
        if(k==0){
            while(total-1>0){
                cur=cur->next;
                total--;
            }
            cur->next=nullptr;
            return head;
        }
        while(k-1>0){
            k--;
            cur=cur->next;
        }
        
        head=cur->next;
        cur->next=nullptr;
        
        return head;
    }
};

